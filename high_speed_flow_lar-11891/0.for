 4300 QXX(1,1) =BB*CHOCHO+AA*SHOSHO 
      QXX(1,2) =RO*SHOCHO*(AA-BB)+BB*USHO-AA*VCHO 
      QXX(1,3) =-VSHO 
      QXX(1,4) =-UCHO 
      QXX(1,5) =-(BB*CHCHO+AA*SHSHO)
