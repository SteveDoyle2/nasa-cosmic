      UNX=QKX/FACT
      UNY=QKY/FACT
      UNZ=QKZ/FACT
      IF(IABS(NDERV).LT.2) GO TO 5000 
      MSIZE=NSIZE+1 
      WX(MSIZE,INXARC)=RX 
C*****    CALCULATE SECOND DERIVATIVES
      GO TO (4100,4200,4300),ISHAPE 
 4100 QXX(1,1) =0.
      QXX(1,2) =0.
      QXX(1,3) =0.
      QXX(2,2) =-(RO+UCHO+VSHO) 
