      GO TO (1100,1200,1300),ISHAPE 
 1100 R=RO/CHOH 
      GO TO 1500
 1200 RFACT=RO*CHOH 
      R=RFACT+SGNR*SQRT(RFACT*RFACT+AA-RORO)
      GO TO 1500
