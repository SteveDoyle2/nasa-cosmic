      RORO=RO*RO
      SHOSHO=SHO*SHO
      SHOCHO=SHO*CHO
      CHOCHO=CHO*CHO
      XOLD=X
      IXARCO=INXARC 
 1050 SH=SIN(H) 
      CH=COS(H) 
      IF(ABS(ABS(H)-PIHALF).LT.1.E-4) CH=0. 
      SHSH=SH*SH
      CHCH=CH*CH
      SHCH=SH*CH
      SHOH=SIN(HO-H)
      CHOH=COS(HO-H)
      SHSHO=SH*SHO
      SHCHO=SH*CHO
      CHSHO=CH*SHO
      CHCHO=CH*CHO
