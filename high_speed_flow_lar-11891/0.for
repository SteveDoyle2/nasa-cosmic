      THE(1)=THE2 
      THE(2)=THE3 
      GO TO 140 
  234 THE(1)=THE3 
      THE(2)=THE4 
      GO TO 140 
  135 IF(I.EQ.2) GO TO 235
      THE(1)=THE3 
      THE(2)=THE4 
      GO TO 140 
  235 THE(1)=THE2 
      THE(2)=THE3 
  140 CONTINUE
