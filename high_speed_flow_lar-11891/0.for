      IF(NDERV.LT.0) GO TO 3000 
      IF(X.EQ.XOLD.AND.INXARC.EQ.IXARCO) GO TO 1050 
      PIHALF=1.5707963267649
      ISHAPE=ICSASH(INXARC,MODEL) 
