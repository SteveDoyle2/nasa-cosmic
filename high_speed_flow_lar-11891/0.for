 4400 GO TO (4410,4420,4420),ISP
C   Z FROM Y
 4410 GO TO (4411,4412),II
C   FIRST POINT 
 4411 Y1(I)=V 
      Y1X(I)=VX 
      Y1XX(I)=VXX 
      Z1(I)=U 
