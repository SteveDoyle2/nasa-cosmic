      GO TO (312,313),I 
  312 IF(ITIME.EQ.2) J1=J1-1
      GO TO 314 
  313 IF(ITIME.EQ.2) J2=J2+1
  314 IF(J1.LT.1.OR.J2.GT.KNTARC.OR.J1.EQ.J2) GO TO 199 
