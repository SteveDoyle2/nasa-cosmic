      GO TO 4500
 4200 QXX(1,1) =1.
      QXX(1,2) =USHO-VCHO 
      QXX(1,3) =0.
      QXX(1,4) =-CHOH 
      QXX(2,2) =RO*(RO+UCHO+VSHO) 
      QXX(2,3) =0.
      QXX(2,4) =USH-VCH 
      QXX(3,3) =0.
      QXX(3,4) =0.
      QXX(4,4) =1.
