      GO TO 4490
C   SECOND POINT
 4422 Y2(I)=U 
      Y2X(I)=UX 
      Y2XX(I)=UXX 
      Z2(I)=V 
      Z2X(I)=VX 
      Z2XX(I)=VXX 
      Y4(I)=US
      Y4X(I)=USX
      Y4XX(I)=USXX
      Z4(I)=VS
      Z4X(I)=VSX
